def make_doc(path: Path, title: str) -> SimpleDocTemplate:
    return SimpleDocTemplate(
        str(path),
        pagesize=A4,
        leftMargin=30 * mm,
        rightMargin=15 * mm,
        topMargin=20 * mm,
        bottomMargin=20 * mm,
        title=title,
        author="Певненко А.А.",
        subject="Магистерская ВКР — МФТИ ФРКТ",
    )


def _page_number(canvas, doc):
    canvas.saveState()
    canvas.setFont("TimesNR", 12)
    canvas.drawCentredString(A4[0] / 2, 10 * mm, str(doc.page))
    canvas.restoreState()


def _page_number_from_two(canvas, doc):
    if doc.page >= 2:
        canvas.saveState()
        canvas.setFont("TimesNR", 12)
        canvas.drawCentredString(A4[0] / 2, 10 * mm, str(doc.page))
        canvas.restoreState()


def p(text, style=STYLE_BODY):
    return Paragraph(text, style)


def spacer(h=12):
    return Spacer(1, h)


def bullets(items, style=STYLE_BULLET):
    return [p(f"— {item}", style) for item in items]


def add_section(story, num, title):
    story.append(p(f"{num} {title}", STYLE_H_SECTION))


def add_chapter(story, num, title):
    story.append(p(f"ГЛАВА {num}", STYLE_H_CHAPTER))
    story.append(p(title.upper(), STYLE_H_CHAPTER))
    story.append(spacer(6))


def struct_heading(story, text):
    story.append(p(text.upper(), STYLE_H_STRUCT))


