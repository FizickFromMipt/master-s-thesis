with col_left:
    st.subheader("Что делает этот инструмент")
    st.markdown(
        """
Безэховые камеры (БЭК) с неудовлетворительными безэховыми условиями
вносят в измеряемую диаграмму обратного рассеяния (ДОР) помеховые
сигналы от переотражений стенок, оснастки и мультипути. Классические
методы — временное стробирование, калибровка, частотная фильтрация —
работают, но ограниченно и плохо описывают *природу* помехи.

Приложение реализует **гибридный пайплайн**:

1. **Сигнальная часть** — ISAR-фокусировка сырых IQ-данных,
   построение range-Doppler изображения, подавление помех средствами
   селекции по дальности и адаптивной фильтрации.
2. **Аналитическая часть** — визуально-языковая модель интерпретирует
   range-Doppler изображение и атрибутирует источники помех
   (переотражения от стен, оснастки, мультипут), формируя
   диагностический отчёт об измерении.
3. **Результат** — восстановленная ДОР с количественной оценкой
   эффективности подавления (PSR, разрешение, погрешность ЭПР)
   и текстовое объяснение, что именно происходило в камере.
        """
    )

    st.subheader("Блок-схема метода")
    st.code(
        """
  IQ (сырые)
      |
      v
  [ ISAR-фокусировка ]
      |
      v
  [ Range-Doppler изображение ]----+
      |                            |
      v                            v
  [ Подавление помех ]      [ VLM-атрибуция ]
      |                            |
      v                            v
  [ Восстановление ДОР ]   [ Диагностический отчёт ]
      \\__________________________/
                  |
                  v
         Итоговый результат
        """,
        language="text",
    )

